def weighted_pick(rng: random.Random, pool: list[str], weights: dict[str, int]) -> str:
    w = [weights.get(a, 1) for a in pool]
    return rng.choices(pool, weights=w, k=1)[0]


def pick_from_card(rng: random.Random, card: list[str], gender: str) -> tuple[str, str]:
    """Pick most & least important from a 5-item attribute card."""
    if gender == "M":
        imp = weighted_pick(rng, card, MALE_IMPORTANT_WEIGHT)
        rest = [a for a in card if a != imp]
        unimp = weighted_pick(rng, rest, MALE_UNIMPORTANT_WEIGHT)
    else:
        imp, unimp = rng.sample(card, 2)
    return imp, unimp


def gen_q3(rng: random.Random, gender: str, verbosity: int) -> tuple[list[int], str]:
    """Generate Q3 multi-select response. Returns (selected_indices, text)."""
    weights = M_TRIGGER_W if gender == "M" else F_TRIGGER_W
    n = {0: 2, 1: 3, 2: 4, 3: 5}.get(verbosity, 3)
    seen: list[int] = []
    for _ in range(n * 6):
        pick = rng.choices(range(len(TRIGGER_OPTIONS)), weights=weights, k=1)[0]
        if pick not in seen:
            seen.append(pick)
        if len(seen) >= n:
            break
    short = [TRIGGER_OPTIONS[i].split(" (")[0] for i in seen]
    if verbosity == 0:
        text = ", ".join(short[:2]) + " 정도요."
    elif verbosity == 1:
        text = ", ".join(short) + " 선택했어요."
    elif verbosity == 2:
        text = ", ".join(short) + " 이렇게 선택했어요."
    else:
        text = (", ".join(short[:3]) + " 이런 것들이요."
                + (" 그리고 " + ", ".join(short[3:]) + "도 선택했어요." if len(short) > 3 else ""))
    return seen, text


def gen_q4(rng: random.Random, gender: str, verbosity: int,
           q3_idx: list[int]) -> tuple[int, str]:
    """Generate Q4 single-select response. Returns (selected_index, text)."""
    weights = M_TRIGGER_W if gender == "M" else F_TRIGGER_W
    best = max(q3_idx, key=lambda i: weights[i]) if q3_idx else rng.randint(0, 14)
    item = TRIGGER_OPTIONS[best].split(" (")[0]
    if verbosity == 0:
        text = f"'{item}'이요."
    elif verbosity == 1:
        text = f"'{item}'로 선택할게요."
    else:
        text = f"제일 큰 계기를 하나만 고르라면 '{item}'인 것 같아요."
    return best, text


