def main() -> None:
    rng = random.Random(20260418_2)

    # 50 male + 150 female = 200 total
    genders = ["M"] * 50 + ["F"] * 150
    rng.shuffle(genders)

    # verbosity distribution: 0=20%, 1=38%, 2=30%, 3=12%  (male max v=2)
    vdist_f = [0] * 20 + [1] * 38 + [2] * 30 + [3] * 12
    vdist_m = [0] * 25 + [1] * 45 + [2] * 30   # male: no v=3
    # pad
    while len(vdist_f) < 150: vdist_f.append(rng.choice([1, 2]))
    while len(vdist_m) < 50:  vdist_m.append(rng.choice([0, 1]))
    rng.shuffle(vdist_f)
    rng.shuffle(vdist_m)

    m_idx = f_idx = 0
    female_names_list = FEMALE_NAMES[:]
    male_names_list   = MALE_NAMES[:]
    rng.shuffle(female_names_list)
    rng.shuffle(male_names_list)

    for i, gender in enumerate(genders):
        if gender == "M":
            name = male_names_list[m_idx % len(male_names_list)]
            v    = vdist_m[m_idx]
            m_idx += 1
        else:
            name = female_names_list[f_idx % len(female_names_list)]
            v    = vdist_f[f_idx]
            f_idx += 1

        phone = fake_phone(rng)
        idx   = i + 53   # files 53 – 252
        path  = OUT_DIR / f"{idx:03d} - 알 수 없는 그룹.txt"
        body  = build(rng, name, phone, gender, v)
        path.write_text(body, encoding="utf-8")
        label = ["과묵", "보통", "수다", "다변"][v]
        print(f"{path.name}  gender={gender}  v={v}({label})")


if __name__ == "__main__":
    main()
