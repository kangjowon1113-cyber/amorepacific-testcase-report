def attr_response(rng: random.Random, a: str, b: str, verbosity: int) -> str:
    if verbosity == 0:
        templates = [
            f"중요한 건 {a}, 안 중요한 건 {b}요.",
            f"{a}이요. {b}은 별로요.",
        ]
    elif verbosity == 1:
        templates = [
            f"가장 중요한 건 {a}이고, 가장 안 중요한 건 {b}인 것 같아요.",
            f"중요한 속성은 {a}이고요, 안 중요한 건 {b}로 할게요.",
        ]
    elif verbosity == 2:
        templates = [
            f"가장 중요한 속성은 {a}입니다. 가장 안 중요한 속성은 {b}이고요.",
            f"중요한 쪽은 {a}이에요. 덜 중요한 건 {b} 정도요.",
        ]
    else:  # 3
        reasons_imp = [
            f"피부한테 직접 영향이 크니까요",
            f"이게 근본적으로 해결돼야 나머지가 따라오는 것 같아서요",
            f"매일 쓰는 제품이다 보니 이게 먼저인 것 같아요",
        ]
        reasons_unimp = [
            f"이건 있으면 좋지만 없어도 크게 상관없더라고요",
            f"이건 저한테는 후순위 같아요",
            f"이 부분은 개인적으로 별로 안 따지는 편이에요",
        ]
        r_i = rng.choice(reasons_imp)
        r_u = rng.choice(reasons_unimp)
        templates = [
            f"음, 가장 중요한 속성은 {a}이에요. {r_i}. 가장 안 중요한 건 {b}이고요, {r_u}.",
            f"가장 중요한 건 {a}인 것 같아요. 가장 덜 중요한 건 {b}이에요. {r_u}.",
        ]
    return rng.choice(templates)


