def fake_phone(rng: random.Random) -> str:
    mid = rng.randint(2000, 9999)
    end = rng.randint(1000, 9999)
    return f"010-{mid:04d}-{end:04d}" if rng.random() < 0.5 else f"010{mid:04d}{end:04d}"


def main() -> None:
    rng = random.Random(20260418)

    # distribute verbosity:  ~20% 과묵, 35% 보통, 30% 수다, 15% 다변
    verbosity_dist = [0] * 10 + [1] * 18 + [2] * 15 + [3] * 7
    # pad/trim to exactly 50
    while len(verbosity_dist) < 50:
        verbosity_dist.append(rng.choice([1, 2]))
    verbosity_dist = verbosity_dist[:50]
    rng.shuffle(verbosity_dist)

    for i in range(50):
        name = NAMES[i % len(NAMES)]
        phone = fake_phone(rng)
        v = verbosity_dist[i]
        idx = i + 3
        path = OUT_DIR / f"{idx:02d} - 알 수 없는 그룹.txt"
        body = build_transcript(rng, name, phone, v)
        path.write_text(body, encoding="utf-8")
        label = ["과묵", "보통", "수다", "다변"][v]
        print(f"{path.name}  (v={v} {label})")


if __name__ == "__main__":
    main()
