def build_transcript(rng: random.Random, name: str, phone: str, verbosity: int) -> str:
    pool = sum(BRAND_POOLS, [])
    rng.shuffle(pool)
    n_brands = {0: 4, 1: 6, 2: 10, 3: 14}[verbosity]
    n_brands = min(n_brands, len(pool))
    brands = pool[:n_brands]
    main_br = rng.choice(brands[:4])
    spf_br = rng.choice(SPF_BRANDS)
    n_prods = {0: 1, 1: 2, 2: 4, 3: 6}[verbosity]
    picks = rng.sample(brands, k=min(n_prods, len(brands)))
    prods = [f"{b} {rng.choice(PRODUCT_TYPES)}" for b in picks]

    def pick(pool_dict: dict) -> str:
        return rng.choice(pool_dict[verbosity])

    lines: list[str] = []
    w = lines.append

    # ── header ──────────────────────────────────────────────────────────────
    w("[사용자 입력]")
    w(f"이름이 무엇인가요?: {name}")
    w(f"사례비 지급을 위하여 연락드릴 수 있는 휴대폰 번호를 적어주세요.: {phone}")
    w("")
    w("-" * 96)
    w("")

    # ── opening ─────────────────────────────────────────────────────────────
    w("[AI 인터뷰어]: 안녕하세요. 오늘은 귀하의 스킨케어 루틴을 이해하고자 인터뷰를 진행하려고 합니다.")
    w("사전에 안내드린 대로, 평소 사용 중이신 스킨 케어 제품을 보여주실 수 있도록 준비가 되셨는지 한 번 더 확인해 주세요.")
    w("")
    w("그럼 인터뷰 시작해도 될까요?")
    w("")
    opening = {
        0: "네.",
        1: "네, 준비됐어요.",
        2: "네, 시작해도 됩니다.",
        3: "네, 다 준비됐어요. 시작해도 됩니다.",
    }[verbosity]
    w(f"[사용자]: {opening}")
    w("")

    # ── Q1 : 스킨케어 신경 ───────────────────────────────────────────────────
    w("[AI 인터뷰어]: 평소 스킨케어에 대해서 신경을 많이 쓰시나요?")
    w("")
    w(f"[사용자]: {pick(CARE_LEVEL)}")
    w("")

    # verbosity 1+ has follow-up on trigger
    if verbosity >= 1:
        w("[AI 인터뷰어]: 그렇군요. 혹시 특별히 스킨케어에 더 신경을 쓰게 된 계기가 있으신가요?")
        w("")
